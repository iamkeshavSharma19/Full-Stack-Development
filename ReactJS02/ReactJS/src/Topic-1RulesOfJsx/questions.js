//&What is React Fragement and empty React Fragement ===

//&React Fragement === It is a wrapper component that lets you group multiple elements without adding an extra DOM node to the HTML output.

//&Empty Fragement(<> </>) === A shorthand syntax for React.Fragment that does the same thing - group elements without extra DOM nodes - but does'not support the key props.

